/*Progama que calcula el área de una circuferencia de 5.2cm*/
public class A6Pg35 {
	public static void main(String[] arg) {
		double radio=5.2, pi=3.14;
		double diametro=radio*radio;
		double area=diametro*pi;
		System.out.println("El diametro de la circuferencia con radio de 5.2 cm es "+area+" cm");
	}
}

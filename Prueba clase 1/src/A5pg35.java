/*Circunferencia de una circunferencia con radio de 3 metros*/
public class A5pg35 {
	public static void main(String[] arg) {
		double radio=3, pi=3.14;
		//Longitud circuferencia=2*PI*RADIO
		double longitud=2*pi*radio;
		System.out.println("La longitud de la circuferencia con radio de 3 metros es "+longitud+" metros");
		
	}

}

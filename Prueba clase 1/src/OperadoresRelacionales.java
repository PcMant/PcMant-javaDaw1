
public class OperadoresRelacionales {
	public static void main (String[]args)
		{int m=2, n=5;
		boolean res;
		res=m>n;
		System.out.println(res);
		res=m<n;
		System.out.println(res);
		res=m>=n;
		System.out.println(res);
		res=m<=n;
		System.out.println(res);
		res=m==n;
		System.out.println(res);
		res=m!=n;
		System.out.println(res);
		}
}
